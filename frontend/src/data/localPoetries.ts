// 根据 ID 获取本地诗词
export function getLocalPoetry(id: number): LocalPoetry | null {
  return LOCAL_POETRIES[id] || null;
}

// 根据标题搜索本地诗词
export function searchPoetryByTitle(title: string): LocalPoetry[] {
  return Object.values(LOCAL_POETRIES).filter(poetry =>
    poetry.title.includes(title)
  );
}

// 根据时期获取本地诗词
export function getPoetriesByPeriod(period: string): LocalPoetry[] {
  return Object.values(LOCAL_POETRIES).filter(poetry =>
    poetry.period === period
  );
}
